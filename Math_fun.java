package com.sagar;
import java.util.Scanner;
public class Math_fun {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		//int n = sc.nextInt();
		
		System.out.println(Math.abs(-25));
		
		System.out.println(Math.ceil(2.9999));
		
		System.out.println(Math.floor(2.9999));

		System.out.println(Math.floor(2.4999));
		
		System.out.println(Math.round(2.9999));
		
		System.out.println(Math.PI);
		
		System.out.println(Math.log(10));
		
		System.out.println(Math.log10(10));
		
		System.out.println(Math.pow(2,3));
		
		System.out.println(Math.max(25, 26));
		
		System.out.println(Math.min(25, 26));
	}

}
